function addParagraphToDiv(divID){
  //This function currently can only be used
  //if the Div only gets one paragraph such
  //that the ID remains unique
  let p     = document.createElement('p');
  p.id      = divID + '_p';
  div       = document.getElementById(divID);
  div.appendChild(p);
  return p;
}//Close addParagraphToDiv

function paragraphAddArticleTitle(p, article){
  p.innerHTML = article["title"];
  linebreak = document.createElement("br");
  p.appendChild(linebreak);
  
}

function paragraphAddArticleLink(p, article){
  let temp_link       = document.createElement("a");
  temp_link.href      = article["url"];
  temp_link.className = "article";
  temp_link.innerHTML = "Published in " + article["journal"];
  //the paragraph gets the link
  p.appendChild(temp_link);
}

